    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String myDriver = "com.mysql.jdbc.Driver";
            String myUrl = "jdbc:mysql://localhost/yosores";
            Class.forName(myDriver);
            Connection conn = DriverManager.getConnection(myUrl, "root", "");

            Statement st = conn.createStatement();

            ResultSet rs = st.executeQuery("select * from tblyorong where first_name=(" + "'" + currentFname + "'" + ");");
            while (rs.next()) {
                currentFname = rs.getString("first_name");
                currentLname = rs.getString("last_name");
                currentMname = rs.getString("middle_name");
                currentEmail = rs.getString("e_mail");
                currentNewPass = rs.getString("new_password");
                currentConPass = rs.getString("confirm_password");
            }
            conn.close();

        } catch (Exception e) {
            System.err.println("Got an exception!");
            System.err.println(e.getMessage());
        }
        jTextArea1.append("First Name: " + currentFname);
        jTextArea1.append("\n");
        jTextArea1.append("Last Name: " + currentLname);
        jTextArea1.append("\n");
        jTextArea1.append("Middle Name: " + currentMname);
        jTextArea1.append("\n");
        jTextArea1.append("Email: " + currentEmail);
        jTextArea1.append("\n");
        jTextArea1.append("Your Password: " + currentNewPass);

    }//GEN-LAST:event_jButton1ActionPerformed

