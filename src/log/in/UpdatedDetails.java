    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        LogInPage ed = new LogInPage();
        int dialogResult = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete your account?");
        if (dialogResult == JOptionPane.YES_OPTION) {
            try {
                String myDriver = "com.mysql.jdbc.Driver";
                String myUrl = "jdbc:mysql://localhost/yosores";
                Class.forName(myDriver);
                Connection conn = DriverManager.getConnection(myUrl, "root", "");

                Statement st = conn.createStatement();

                st.executeUpdate("delete from tblyorong where first_name=(" + "'" + currentFname + "'" + ");");
                JOptionPane.showMessageDialog(null, "Your account is deleted!");
                this.dispose();
                ed.setVisible(true);

                conn.close();

            } catch (Exception e) {
                System.err.println("Got an exception!");
                System.err.println(e.getMessage());
            }
        } else {
            this.dispose();
            ed.setVisible(true);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

